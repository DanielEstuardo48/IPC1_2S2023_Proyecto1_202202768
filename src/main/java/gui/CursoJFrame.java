    private void CargamasivaalumnosjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CargamasivaalumnosjButton1ActionPerformed
        JFileChooser fileChooser = new JFileChooser();
						  fileChooser.setMultiSelectionEnabled(true);
        FileNameExtensionFilter csvFilter = new FileNameExtensionFilter("Archivos CSV", "csv");
        fileChooser.setFileFilter(csvFilter);

        int fileChooserState = fileChooser.showOpenDialog(this);
        if (fileChooserState == JFileChooser.APPROVE_OPTION) {
            File[] archivos = fileChooser.getSelectedFiles();
            for (File file : archivos) {
                try {
                    BufferedReader reader = new BufferedReader(new FileReader(file));
                    String linea;

                    while ((linea = reader.readLine()) != null) {
                        String[] datos = linea.split(",");
                        String codigoestu = datos[0];
                        /*String nombreestu = datos[1];
                        String apellidoestu = datos[2];
                        String correoestu = datos[3];
                        String generoestu = datos[4];*/
                        //Se agrega a profesores
                        for(int i = 0; i<AppState.getEstudiantes().size();i++){
                            Estudiantes actu = AppState.getEstudiantes().get(i);
                            if(actu.getCodigoe().equals(codigoestu)){
                                Estudiantes estudiante = new Estudiantes(codigoestu, actu.getNombree(),
                                        actu.getApellidoe(), "", "", 
                                        "", "", "");
                            }
                        }
                        /*Estudiantes estudiante = new Estudiantes(
                                codigoestu, nombreestu, apellidoestu,
                                correoestu, "1234", generoestu,
                                codigoestu, "1234"
                        );
                        AppState.addEstudiante(estudiante);
                        curso.addEstudiante(estudiante.codigoe);*/
                    }
                } catch (IOException e) {
                    e.printStackTrace();
                    System.out.println("Error al Cargar Archivo");
                }
            }
            llenartablaalumnos();
        }
    }//GEN-LAST:event_CargamasivaalumnosjButton1ActionPerformed

