    public void llenartablaact(){
        int tamano = curso.actividades.size();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setRowCount(tamano);
        // Se agregaran las columnas
        modelo.addColumn("Nombre");
        modelo.addColumn("Descripcion");
        modelo.addColumn("Ponderacion");
        modelo.addColumn("Promedio");
        //datos
        for(int i = 0; i < tamano; i++){
            Actividad actividad = Curso.actividades.get(i);
            modelo.setValueAt(actividad.getNombre(), i, 0);
            modelo.setValueAt(actividad.getDescripcion(), i, 1);
            modelo.setValueAt(actividad.getPonderacion(), i, 2);
            modelo.setValueAt("", i, 3);
        }
        this.ActividadesjTable1.setModel(modelo);
    }
    
